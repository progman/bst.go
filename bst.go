type bst_item_t struct {

	key int
	l *bst_item_t
	r *bst_item_t
}
/* ***************************************************************************************************************************************************************************************************************************************************************************************************************** */
func (p *bst_item_t) init(key int) {

	p.key = key
	p.l   = nil
	p.r   = nil
}
/* ***************************************************************************************************************************************************************************************************************************************************************************************************************** */
type bst_t struct {
	head *bst_item_t
}
/* ***************************************************************************************************************************************************************************************************************************************************************************************************************** */
func (p *bst_t) init() {

	p.head = nil
}
/* ***************************************************************************************************************************************************************************************************************************************************************************************************************** */
func (p *bst_t) insert(key int, flag_uniq bool) {

	log.Printf("insert(%d)\n", key)
	var p_bstr_item *bst_item_t = &bst_item_t{}
	list = append(list, p_bstr_item)


	p_bstr_item.init(key)


	if (p.head == nil) {

		log.Printf("\tset as root\n")
		p.head = p_bstr_item
		return
	}


	var p_cur *bst_item_t = p.head
	for {

// rigth way
		if (key > p_cur.key) {

			if (p_cur.r == nil) {

				log.Printf("\tset rigth\n")
				p_cur.r = p_bstr_item
				break;

			} else {

				log.Printf("\tgo to rigth\n")
				p_cur = p_cur.r
				continue;
			}
		}

// left way
		if (key < p_cur.key) {

			if (p_cur.l == nil) {

				log.Printf("\tset left\n")
				p_cur.l = p_bstr_item
				break;

			} else {

				log.Printf("\tgo to left\n")
				p_cur = p_cur.l
				continue;
			}
		}

// equal way. we can skip it (use uniq values) or swap it (use non uniq values), swap by default
		if (key == p_cur.key) {

			if (flag_uniq == false) {

				log.Printf("\tswap\n")
				p_bstr_item.l = p_cur.l
				p_cur.l = p_bstr_item
				break

			} else {

				log.Printf("\tskip\n")
				break
			}
		}
	}
}
